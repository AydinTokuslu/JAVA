package sorucozumleri_05_temmuz_2021;

public class sorucozumleri01 {

	public static void main(String[] args) {
		
		  /*
      \n (or \r): starts from a new line : metnş bir alt satırdan itibaren yazdırır.
              \t:  horizontal tab : metni yatayda 1 tab miktarı kaydırır.
              \\: prints a back slash :\ (ters slash) yazdırır
              \': prints single quote :' tek tırnak yazdırır.
              \": prints double quote :"" Çift tırnak yazdırır.
     */

		// Soru-1 :   konsola      "Hello ", "World  \ /"        seklinde yazdırınız
		System.out.println("\"Hello \", \"Word \\ / \"");
		        
		        // Soru-2 :   isminizin her harfini ayrı bir satira gelecek sekilde yazdırınız.
		System.out.println("h\na\nl\nu\nk");
		        
		        // Soru-3 : "Java" ile hayat cok 'afilli'   yazdırınız
		        
		System.out.println(" \"java\" ile hayat cok \'afilli\'");
		        // Soru-4 : "Basari" gayrete
		        //           'asiktir'                 yazdırınız sonraki "TechProEd ile java cok kolay ..." ifadesi  3 satır sonra ve satır basından 1 tab ilerideyazılsın.
		        
		        
		        System.out.println("\"Basarı\" gayrete \n\'asiktir\'");
		        
		        
		        System.out.println("\n\n\n");
		         System.out.println("\tTechProED ile java cok kolay");
		
		
	}

}
