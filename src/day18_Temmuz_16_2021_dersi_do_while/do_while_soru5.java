package day18_Temmuz_16_2021_dersi_do_while;

public class do_while_soru5 {

	public static void main(String[] args) {
		// ’m’ harfinden baslayarak ‘c’ harfine kadar tum harfleri yazdirin.

		char m = 'm';
		char c = 'c';

		while (c <= m) {
			System.out.print(m + " ");

			m--;
		
		}
		
		
		
		
		
		
	}

}
